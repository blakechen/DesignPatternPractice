package idv.blake;

public class PersonData {
    String name;
    int hp;

    public void setName(String name) {
        this.name = name;
    }

    public void setHP(int hp) {
        this.hp = hp;
    }

    public String getName() {
        return name;
    }

    public int getHP() {
        return hp;
    }
}
